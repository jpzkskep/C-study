/*
다음 조건을 만족하는 프로그램을 작성하라:

정수형 변수 a = 100, b = 200, c = 300이 있다.

포인터 배열 arr을 선언하여, a, b, c의 주소를 각각 arr[0], arr[1], arr[2]에 저장한다.

반복문을 사용하여 arr에 저장된 포인터를 통해 각각의 값을 출력하라.
(즉, *arr[0], *arr[1], *arr[2]를 반복문으로 처리)

조건:

포인터 배열을 선언할 때 크기 명시

단일 포인터가 아닌 "포인터 배열" 개념을 사용

배열 인덱싱과 포인터 역참조(*) 개념을 함께 쓸 것
*/
#include <stdio.h>
int main() {
	int a = 100, b = 200, c = 300;
	int *arr[3];
	arr[0] = &a;
	arr[1] = &b;
	arr[2] = &c;
	for (int i = 0; i < 3; i++) {
		printf("*arr[%d] = %d \n", i, *arr[i]);
	}
	return 0;
}